<h1>About the network</h1>

<div class="row">
	<div class="span8">
		<p class="hero-unit">
			Hyperboria is a decentralised network established with the prinicples of freedom and security at its core.
		</p>
		<p>
			The network is composed of nodes and the encrypted tunnels between them. These tunnels travel over
			both traditional internet infrstructure and wireless networks, where available. 
		</p>
		<p>
			The Hyperboria network is part of an international effort to protect free speech, called 
			<a href="http://projectmeshnet.org">Project Meshnet</a>.
		</p>
